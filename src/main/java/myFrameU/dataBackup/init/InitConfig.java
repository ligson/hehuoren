package myFrameU.dataBackup.init;



public class InitConfig{
	private Class initMavenClass;
	
	private String mysqlpath;
	private String sqlPath;
	private String address;
	private String databaseName;
	private String username;
	private String password;
	public Class getInitMavenClass() {
		return initMavenClass;
	}
	public void setInitMavenClass(Class initMavenClass) {
		this.initMavenClass = initMavenClass;
	}
	public String getMysqlpath() {
		return mysqlpath;
	}
	public void setMysqlpath(String mysqlpath) {
		this.mysqlpath = mysqlpath;
	}
	public String getSqlPath() {
		return sqlPath;
	}
	public void setSqlPath(String sqlPath) {
		this.sqlPath = sqlPath;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDatabaseName() {
		return databaseName;
	}
	public void setDatabaseName(String databaseName) {
		this.databaseName = databaseName;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
	
}

package myFrameU.biz.util;

import java.util.List;

public class ModifyProperty {
	private String key;
	private String value;
	private List<String> ruleMethodList;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public List<String> getRuleMethodList() {
		return ruleMethodList;
	}
	public void setRuleMethodList(List<String> ruleMethodList) {
		this.ruleMethodList = ruleMethodList;
	}
	
}

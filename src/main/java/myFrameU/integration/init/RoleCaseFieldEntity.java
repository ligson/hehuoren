package myFrameU.integration.init;

public class RoleCaseFieldEntity {
	private String roleName;
	private String gradeField;
	private String totalField;
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getGradeField() {
		return gradeField;
	}
	public void setGradeField(String gradeField) {
		this.gradeField = gradeField;
	}
	public String getTotalField() {
		return totalField;
	}
	public void setTotalField(String totalField) {
		this.totalField = totalField;
	}
	
}
